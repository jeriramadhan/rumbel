            <h4 class="title">
              3. Berikan kucing Anda vaksin.</h4>
            <div class="content">
              Meskipun kucing Anda tidak keluar rumah, Anda tetap harus memberinya vaksin. Beberapa infeksi virus,
              seperti feline distemper, disebabkan oleh virus kuat yang berasal dari sepatu Anda. Selain itu, Jika
              kucing menyelinap keluar di malam hari, kucing Anda berisiko terjangkit penyakit karena sistem imunnya
              belum pernah terkena serangan dari luar rumah.
              <br>
              <li>Kucing yang tidak pernah berburu atau pergi keluar rumah tetap membutuhkan obat cacing setiap dua kali
                dalam setahun. Hal ini disebabkan karena kucing terlahir dengan cacing-cacing yang didapatkan dari
                induknya dan telur-telur cacing yang tertinggal di dalam jaringan tubuhnya akan menetas sepanjang usia
                kucing Anda.</li>
