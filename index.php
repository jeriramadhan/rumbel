          <div class="content_wrapper">
            <h3>Bagian 1: Menjaga Kucing Tetap Aman </h3>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/1.jpg" alt="">
            </div>
            <h4 class="title">1. Buatlah lingkungan yang aman bagi kucing</h4>
            <div class="content">
              Kucing rumahan terbiasa menghibur dirinya sendiri, jadi ia akan menjelajahi rumah dan bermain dengan apa
              pun yang ia lihat. Sangatlah penting untuk memeriksa rumah dengan menggunakan sudut pandang si kucing
              untuk mencari sesuatu yang bisa menyakiti kucing Anda dan memastikan bahwa ia aman.
              <br>
              <li>Waspadalah karena kucing mungkin akan melompat ke atas berbagai jenis permukaan. Hanya karena tinggi
                permukaan tersebut setinggi pinggang, atau bahkan setinggi kepala, bukan berarti kucing Anda tidak akan
                menjelajahinya.</li>
              <li>Singkirkan benda-benda yang menarik bagi kucing Anda. Contohnya, benda berkilap, tali sepatu, wol,
                tali, semua peralatan menjahit sangatlah menarik bagi kucing. Jika benda tersebut panjang, tipis, dan
                berbentuk seperti ular, kucing akan merasa tertarik untuk bermain dengannya. Pastikan untuk merapikan
                semua benda tersebut karena jika kucing menggigit-gigigt benda tersebut dengan mulutnya dan menelan
                potongan wol, ia bisa terjangkit gangguan usus serius dan harus dioperasi.</li>
              <li>Kucing dan lilin bukanlah pasangan yang baik. Kucing senang melompat, jadi, menaruh lilin diatas rak
                yang tinggi belum tentu aman. Kemungkinan terburuknya, kucing tidak tahu bahwa ada lilin di sana,
                sehingga ia akan menyenggolnya dan menyebabkan kebakaran.</li>
            </div>
            <br>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/2.jpg" alt="">
            </div>
            <h4 class="title">
              2. Jauhkan kucing dari racun</h4>
            <div class="content">
              Selalu pastikan untuk menyimpan alat-alat kebersihan dan zat-zat kimia lainnya di tempat yang aman.
              Terdapat banyak bahan kimia berbahaya yang kita gunakan di rumah dan sangat beracun bagi kucing.
              <br>
              <li>Contohnya, banyak pembersih lantai yang mengandung benzalkonium klorida. Bahan kimia ini bisa
                menyebabkan luka bakar di jaringan yang lembut seperti lidah atau garis mulut kucing. Jika kucing
                berjalan di lantai yang masih basah oleh pembersih lantai lalu menjilati cakarnya, hal ini bisa
                menyebabkan bisul parah di lidahnya yang menghalangi kucing untuk makan.</li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/3.jpg" alt="">
            </div>
            <h4 class="title">
              3.
              Selidikilah apakah tanaman di rumah Anda beracun bagi kucing</h4>
            <div class="content">
              Banyak tanaman yang beracun bagi kucing. Bunga bakung tidak pantas ditempatkan di lingkungan kucing karena
              serbuk sari bunga bakung sangat beracun dan bisa mengakibatkan gagal ginjal yang parah. Tanaman lain yang
              harus dihindari adalah azalea, hydrangea, poinsettia, daffodil, dan mistletoe. Tanaman-tanaman tersebut
              bukanlah daftar tanaman beracun yang lengkap, jadi periksalah selalu apakah tanaman Anda berbahaya untuk
              kucing atau tidak sebelum membawanya ke rumah.
              <br>
            </div>
            <h3>Bagian 2: Menyediakan Kebutuhan-Kebutuhan Dasar Kucing</h3>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/4.jpg" alt="">
            </div>
            <h4 class="title">
              1. Sediakan kotak pasir sebagai tempat buang air bagi kucing Anda yang bisa dibersihkan setiap hari.</h4>
            <div class="content">
              Kucing membutuhkan privasi saat buang air. Tempatkan kotak pasir di tempat sepi sehingga si kucing tidak
              akan terganggu atau ketakutan oleh suara yang tiba-tiba muncul (seperti suara mesin cuci). Jika Anda
              memiliki beberapa kucing, Anda membutuhkan satu kotak pasir untuk setiap kucing. Ada baiknya Anda memiliki
              kotak pasir cadangan. Jadi, jika Anda memiliki lima ekor kucing, sebaiknya sediakan enam buah kotak pasir.
              <br>
              <li>Jagalah kotak pasir agar tetap bersih sehingga kucing akan senang mendatanginya. Artinya, Anda harus
                menyekop kotoran dan membersih noda setiap hari serta membersihkan kotak pasir secara menyeluruh setiap
                minggu.</li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/5.jpg" alt="">
            </div>
            <h4 class="title">
              2. Berilah kucing makanan dan air</h4>
            <div class="content">
              Ketahuilah bahwa kucing rumahan sangat mudah gendut. Untuk mencegah ini, takarlah makanannya dan berikan
              sesuai dengan takaran yang di sarankan pada kemasan. Jika kucing bertambah berat badannya (tulang rusuknya
              akan lebih sulit untuk ditemukan), kurangilah jatah harian kucing Anda.
              <br>
              <li>Selalu sediakan air tawar SETIAP SAAT! Hal ini sangatlah penting.</li>
              <li>Pertimbangkan makanan kucing untuk kucing “rumahan” atau “steril”, karena makanan ini mengandung
                jumlah kalori yang lebih sedikit sehingga cocok untuk gaya hidup kucing Anda.</li>
              <li>Asah insting berburu kucing dengan menggunakan permainan teka-teki sehingga kucing harus berusaha
                untuk mendapatkan makanannya. Mainan ini tersedia di internet dan di kebanyakan toko hewan peliharaan.
              </li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/6.jpg" alt="">
            </div>
            <h4 class="title">
              3. Berilah kucing tempat khusus untuk menghabiskan waktunya sendiri</h4>
            <div class="content">
              Penting bagi kucing untuk mempunyai ruang spesial miliknya sendiri, bahkan ketika ia tidur bersama Anda di
              malam hari. Ruang kecil ini bisa berupa tempat tidur kucing atau pohon kucing sehingga kucing Anda bisa
              meringkuk di dalamnya.
              <br>

            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/7.jpg" alt="">
            </div>
            <h4 class="title">
              4.
              Sediakanlah tiang garukan sebagai tempat kucing mencakar</h4>
            <div class="content">
              Mencakar adalah bagian dari tingkah laku normal kucing. Saat kucing mencakari sesuatu, ia melepaskan
              sedikit aroma yang menandakan kepada kucing lain bahwa ia telah menyatakan daerahnya. Jika Anda tidak
              menyediakan kucing tiang garukan untuk mencakar, kucing Anda akan mencari tempat mencakar sendiri dan
              mungkin saja ia akan menggunakan sofa terbaik Anda.
              <br>
              <li>Perhatikan apakah kucing mencakar secara horizontal (sepanjang karpet) atau vertikal (ke atas sofa).
                Sediakan tiang penggaruk yang horizontal atau vertikal untuk menyesuaikan dengan pilihan tempat natural
                bagi kucing untuk mencakar.</li>
              <li>Pastikanlah tiangnya lebih tinggi dari kucing Anda dan tempatkan dengan kuat, sehingga tiang tersebut
                tidak akan bergerak saat kucing Anda mencakarinya. Tempatkan tiang tersebut di dekat pintu masuk atau
                keluar karena ini merupakan tempat favorit bagi kucing untuk melepaskan aroma mereka.</li>
              <li>Tempatkan juga tiang di dekat tempat tidur kucing karena kucing gemar mencakari sesuatu saat bangun
                tidur.</li>

            </div>
            <h3>Bagian 3: Memberikan Kucing Stimulasi Mental dan Olahraga</h3>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/8.jpg" alt="">
            </div>
            <h4 class="title">
              1. Pastikan kucing mendapatkan olahraga yang cukup</h4>
            <div class="content">
              Bermainlah dengan kucing secara rutin. Pastikan Anda memiliki jadwal bermain rutin setiap beberapa kali
              sehari. Kucing adalah pelari cepat, bukan pelari maraton, tiga atau empat sesi berdurasi sekitar lima
              sampai sepuluh menit lebih baik daripada satu sesi selama 20 menit.
              <br>
              <li>Sediakanlah tongkat bulu kecil, tikus mainan, atau bahkan sehelai benang (kucing tidak akan pernah
                bosan memainkannya). Goyangkan tongkat bulu setinggi kucing Anda. Lemparkan tikus mainan dan perhatikan
                si kucing bergulat dengannya. Pegangi ujung benang dan berlarilah ke sekeliling rumah atau goyangkan
                saja benang tersebut.</li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/9.jpg" alt="">
            </div>
            <h4 class="title">
              2. Jangan mengira bahwa kucing tidak mau bermain hanya karena ia gemar tidur siang</h4>
            <div class="content">
              Kucing adalah pemburu yang berarti mereka memiliki periode untuk bermalas-malasan yang diselingi dengan
              aktivitas berburu. Hanya karena kucing gemar untuk tidur bukan berarti mereka tidak membutuhkan stimulasi
              mental yang didapat dengan mencium bau tikus, menguntit, dan menangkapnya
              <br>
              <li>Banyak masalah tingkah laku seperti mengotori rumah, mencakari sesuatu, atau agresi terhadap hewan
                peliharaan lain disebabkan oleh kebosanan dan rasa frustasi. Pastikan untuk menyediakan beberapa benda
                menarik yang bisa si kucing mainkan..</li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/10.jpg" alt="">
            </div>
            <h4 class="title">
              3. Berilah kucing pemandangan di luar rumah</h4>
            <div class="content">
              Pastikan tempatnya memiliki tenggeran yang tinggi serta jendela. Hal ini akan menghibur si kucing dengan
              pemandangan di luar serta untuk memenuhi kebutuhan kucing untuk berada di tempat tinggi, karena kucing
              adalah pemnajat pohon yang alami
              <br>
              <li>Jika Anda tinggal di apartemen, pastikanlah balkon Anda aman bagi kucing dan ia tidak akan jatuh dari
                pagar balkon. Anda boleh mempertimbangkan untuk memagari balkon sehinggga si kucing bisa memperhatikan
                lingkungan tanpa risiko cedera.</li>
              <li>Selain itu, pastikanlah setiap jendela sudah aman dan tidak bisa dilewati kucing. Periksalah
                sekat-sekat untuk memastikan tempat tersebut aman dan tidak rusak sehingga kucing tidak bisa menyelinap
                keluar saat Anda tidak melihat.</li>
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/11.jpg" alt="">
            </div>
            <h4 class="title">
              4. Bangunlah ikatan dengan kucing melalui latihan</h4>
            <div class="content">
              Bukan hanya anjing yang mendapatkan keuntungan dari latihan kepatuhan. Cobalah untuk melatih kucing Anda
              dengan clicker, yang memberikan stimulasi mental penting untuk kucing dan membantunya untuk membangun
              ikatan dengan Anda. Latihan ini hampir sama dengan melatih anjing, tetapi Anda harus bersabar dan
              mengetahui suguhan apa yang sangat memotivasi si kucing untuk mengikuti perintah Anda.
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/12.jpg" alt="">
            </div>
            <h4 class="title">
              5. Berikan kucing Anda sesuatu untuk dimainkan saat Anda sedang pergi.</h4>
            <div class="content">
              Tinggalkan mainan untuk kucing Anda saat ia tidak bisa bermain dengan Anda. Anda bisa meninggalkan tikus
              mainan atau catnip, tetapi Anda juga bisa meninggalkan benda-benda baru yang aman bagi kucing. Cobalah
              tinggalkan kantong kertas atau kotak kardus untuk kucing Anda jelajahi.
              <br>
            </div>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/13.jpg" alt="">
            </div>
            <h4 class="title">
              6.
              Nikmatilah waktu bersama kucing Anda!</h4>
            <div class="content">
              Berikan ia cinta dan perhatian jika ia menginginkannya. Anda memiliki sekitar 20 tahun untuk bersama
              kucing kesayangan Anda, jadi lakukanlah yang terbaik dengan membangun ikatan untuk saling menyayangi.
              <br>
            </div>
            <h3>Bagian 4: Menjaga Kesehatan Kucing</h3>
            <div class="main_image">
              <img class="img-fluid" src="img/panduan/14.jpg" alt="">
            </div>
            <h4 class="title">
              1. Sekali-kali, berikan kucing Anda perawatan.</h4>
            <div class="content">
              Kuku kucing rumahan harus lebih sering diguting karena kucing rumahan tidak menggunakannya saat pergi
              keluar. Anda bisa menggunakan jasa dari dokter hewan atau pegawai salon hewan untuk memotongnya atau Anda
              juga bisa meminta saran dokter untuk melakukannya. Potonglah ujung kuku sebulan sekali.
              <br>
              <li>Sisiri kucing Anda di tempat yang ia mau. Kucing mungkin akan berguling dan membiarkan Anda menyisiri
                perutnya jika ia sedang manja! Menyisiri kucing umumnya tidak dibutuhkan untuk menjaga rambutnya dalam
                kondisi yang baik (kucing biasanya merawatnya sendiri) tetapi hal ini merupakan cara yang baik untuk
                membangun ikatan dengan kucing. Hal ini juga merupakan kesempatan untuk memeriksa apakah ada gejala
                masalah kesehatan pada bulu dan tubuh kucing.</li>
              <li>Carilah titik-titik hitam atau sesuatu yang bergerak pada bulu kucing. Pisahkan bulunya dan carilah
                kulit yang terbuka. Jika Anda melihat sesuatu yang bergerak atau banyak titik-titik hitam (kotoran
                kutu), berikan kucing Anda kalung atau semprotan antikutu atau bawalah kucing Anda ke dokter hewan untuk
                mendapatkan masukan mengenai perawatannya.</li>
              <li>Terkadang ketika kucing menua, ia tidak akan mampu untuk merawat keseluruhan bulunya. Kucing akan
                kehilangan fleksibilitasnya sehingga membuatnya sulit untuk menjangkau bulunya secara keseluruhan. Jika
                ini yang terjadi pada kucing Anda, Anda mungkin harus merawatnya dengan lebih teratur untuk menjaga
